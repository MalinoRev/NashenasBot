def get_message(min_age: int | None) -> str:
	base = (
		"با فیلتر درخواست چت میتونی تو سه مرحله مشخص کنی کاربران با چه جنسیتی تو چه فاصله ازت و چه رده سنی بتونن بهت درخواست چت بدن\n\n"
		"مرحله سوم (فیلتر سنی) :\n"
	)
	prefix = f"حداقل سن انتخاب شده: {min_age}\n" if min_age else "بدون محدودیت حداقل سن\n"
	return base + prefix + "تا چه سنی میتونن بهت درخواست چت بدن؟"


