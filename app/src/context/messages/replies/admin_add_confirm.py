def get_message(user_name: str, user_id: int) -> str:
	return (
		f"✅ **تایید اضافه کردن ادمین**\n\n"
		f"👤 **نام کاربر**: {user_name}\n"
		f"🆔 **آیدی**: `{user_id}`\n\n"
		"آیا مطمئن هستید که می‌خواهید این کاربر را به عنوان ادمین اضافه کنید؟"
	)


def get_user_not_found_message() -> str:
	return (
		"❌ **کاربر یافت نشد!**\n\n"
		"کاربری با این آیدی در سیستم یافت نشد.\n"
		"لطفاً آیدی صحیح را وارد کنید یا از دکمه بازگشت استفاده کنید."
	)


