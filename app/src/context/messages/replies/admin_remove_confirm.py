def get_message(user_name: str, user_id: int) -> str:
	return (
		f"⚠️ **تایید حذف ادمین**\n\n"
		f"👤 **نام ادمین**: {user_name}\n"
		f"🆔 **آیدی**: `{user_id}`\n\n"
		"آیا مطمئن هستید که می‌خواهید این ادمین را حذف کنید؟\n"
		"⚠️ **این عمل قابل بازگشت نیست!**"
	)


